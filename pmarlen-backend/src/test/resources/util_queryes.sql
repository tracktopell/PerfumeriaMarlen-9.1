SELECT 	PVE.PEDIDO_VENTA_ID,MAX(PVE.ESTADO_ID) ULT_ESTADO 
FROM 	PEDIDO_VENTA_ESTADO PVE,PEDIDO_VENTA PV 
WHERE	PVE.PEDIDO_VENTA_ID = PV.ID
AND		PV.ALMACEN_ID = 1
GROUP BY 	PEDIDO_VENTA_ID;


SELECT 	PEDIDO_VENTA_ID,ULT_ESTADO 
FROM 	(SELECT 	PVE.PEDIDO_VENTA_ID,MAX(PVE.ESTADO_ID) ULT_ESTADO 
FROM 	PEDIDO_VENTA_ESTADO PVE,PEDIDO_VENTA PV 
WHERE	PVE.PEDIDO_VENTA_ID = PV.ID
AND		PV.ALMACEN_ID = 1
GROUP BY 	PEDIDO_VENTA_ID) UTL_ESTADOS 
WHERE 	ULT_ESTADO < 5;

SELECT 	SUM(CANTIDAD) ACUMULADO_CANTIDAD, PRODUCTO_ID 
FROM 	PEDIDO_VENTA_DETALLE 
WHERE 	PEDIDO_VENTA_ID IN (475,476) 
GROUP BY 	PRODUCTO_ID  
