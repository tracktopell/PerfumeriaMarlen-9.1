SELECT 	PRODUCTO_ID, SUM(CANTIDAD) CANTIDAD_TRANCITO
FROM 	PEDIDO_VENTA_DETALLE PVD, (SELECT 	PEDIDO_VENTA_ID,ULT_ESTADO 
FROM 	(SELECT 	PVE.PEDIDO_VENTA_ID,MAX(PVE.ESTADO_ID) ULT_ESTADO 
FROM 	PEDIDO_VENTA_ESTADO PVE,PEDIDO_VENTA PV 
WHERE	PVE.PEDIDO_VENTA_ID = PV.ID
AND		PV.ALMACEN_ID = 1
GROUP BY 	PEDIDO_VENTA_ID) UTL_ESTADOS 
WHERE 	ULT_ESTADO < 5) PT
WHERE 	PVD.PEDIDO_VENTA_ID = PT.PEDIDO_VENTA_ID 
GROUP BY 	PVD.PRODUCTO_ID  
ORDER BY  PVD.PRODUCTO_ID;

